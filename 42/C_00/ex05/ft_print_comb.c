#include <unistd.h>

void	ft_print_comb(void)
{
	char	s1;
	char	s2;
	char	s3;

	s1 = '0';
	while (s1 <= '7')
	{
		s2 = s1 + 1;
		while (s2 <= '8')
		{
			s3 = s2 + 1;
			while (s3 <= '9')
			{
				write(1, &s1, 1);
				write(1, &s2, 1);
				write(1, &s3, 1);
				if (!(s1 == '7' && s2 == '8' && s3 == '9'))
					write(1, ", ", 2);
				s3++;
			}
			s2++;
		}
		s1++;
	}
}
