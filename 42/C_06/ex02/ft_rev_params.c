#include <unistd.h>

int main(int argc, char argv[])
{
	int	i;

	i = argc;
	while (i >= argc)
	{
		if (i<=0)
		{
			break;
		}
		write(1, argv[i-1], 1);
		i--;
	}
	return (0);
}
